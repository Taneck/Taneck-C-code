//删除数组中指定的元素
//不新开辟空间！
int main()
{
	int arr[10] = { 1,2,3,2,3,4,5,6,3,2 };
	int del;
	int i = 0;
	int j = 0;
	scanf("%d", &del);
	for (i = 0; i < 10; i++)
	{
		if (arr[i] != del)
		{
			arr[j] = arr[i];
			j++;
		}
	}
	for (i = 0; i < j; i++)
	{
		printf("%d ", arr[i]);
	}
	return 0;
}